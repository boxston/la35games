                    <h2>Visión</h2>
                    <p>
                        Apoyados en nuestros valores, nuestra
                        visión es la de una sociedad mas justa, en
                        la que todos sus miembros tengan las
                        mismas oportunidades de crecimiento y
                        puedan acceder a los mismos bienes y
                        servicios, que las diferencias sociales
                        puedan ser subsanadas, y que ese estado
                        de igualdad sea un punto de partida para
                        el crecimiento de la humanidad.
                    </p>
                </article>
                <article>
                    <img src="/public/img/iconos/vision_icon.svg">
                    <h2>Misión</h2>
                    <p>
                        Fomentar y consolidar un espacio de
                        trabajo apto para el desarrollo y el
                        crecimiento de cada uno de los
                        integrantes de nuestro Mutual, para
                        trabajar juntos dia a dia con acciones
                        concretas en el corto, mediano y largo
                        plazo que logren satisfacer las
                        necesidades de nuestros asociados. A
                        partir de convenios con múltiples
                        empresas de primer nivel en distintos
                        rubros, crear el camino necesario para
                        poner al alcance de la sociedad servicios
                        de calidad y al menor costo posible.
                    </p>
                </article>
                <article>
                    <img src="/public/img/iconos/nuestros_valores_icon.svg">
                    <h2>Nuestros Valores</h2>
                    <p>
                        Convencidos de que todo crecimiento tiene
                        sus bases en los firmes valores de los
                        miembros de una sociedad, enfocamos
                        nuestro camino en: <b>La Solidaridad</b>, para con
                        los afiliados y la sociedad en general; <b>la
                        Honestidad</b>, en la transparencia de los
                        procedimientos y la comunicación de
                        nuestros actos; <b>la Idoneidad</b>, para
                        solucionar de manera ágil y eficaz las
                        necesidades de nuestros socios y su grupo
                        familiar; y <b>la Ética Profesional</b> necesaria
                        para tomar conciencia de la
                        responsabilidad y la confianza que nuestros
                        asociados ponen en nuestras manos.
                    </p>
                </article>
